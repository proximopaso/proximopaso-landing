// Input de Texto
export const TextInput = ({ label, placeholder, isTextArea = false, value, onChange, name }) => {
    const baseStyles = {
        width: '100%',
        padding: 'var(--space-md) var(--space-lg)',
        background: 'var(--white)',
        borderRadius: 'var(--radius-md)',
        border: '1px solid var(--primary-10)',
        color: 'var(--text-gray)',
        fontSize: 13,
        fontFamily: 'inherit',
        boxSizing: 'border-box',
        outline: 'none',
        resize: isTextArea ? 'vertical' : 'none'
    };

    return (
        <div style={{ alignSelf: 'stretch', display: 'flex', flexDirection: 'column', marginBottom: 'var(--space-xs)' }}>
            <QuestionLabel text={label} />
            {isTextArea ? (
                <textarea
                    name={name}
                    placeholder={placeholder}
                    value={value}
                    onChange={onChange}
                    style={{ ...baseStyles, minHeight: 100 }}
                />
            ) : (
                <input
                    type="text"
                    name={name}
                    placeholder={placeholder}
                    value={value}
                    onChange={onChange}
                    style={baseStyles}
                />
            )}
        </div>
    );
};

